    <h1 style="text-align: center">ORDU</h1><br>
    <p>Karadeniz Bölgesi’nin Orta ve Doğu Karadeniz bölümünde yer alan Ordu
        Türkiye’nin en kalabalık otuzuncu şehridir. Doğal güzelliklerinin
        yanı sıra tarihi zenginlikleri ile öne çıkar. 19 ilçeye sahiptir.
        2014 yılında büyükşehir olmuştur ve plaka kodu 52’dir.</p><br>

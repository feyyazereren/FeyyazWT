    <h1 style="text-align: center">Hakkında</h1>
    <p>1960'lı yılların ikicisi yarısı.
        Anadolu'nun birçok kentinde futbol bilinmezken, tribünlerinde kadınların coşkulu tezahüratlarıyla maç izlediği bir şehir Ordu.</p><br>

        <img src="../assets/img/orduspor/ali_ataoglu.jpg" alt="Ali Ataoğlu"
             class="center">
        <br><br>
        <p style="text-align: center">
            İstanbulspor'da, daha sonra da Anadoluhisarı'nda forma giyen Ali Ataoğlu (Kara Ali), Ordu'ya döndükten bir süre sonra kendisinin de forma giydiği Spor Yıldızı takımını kurdu.
        </p>
    <p> İlk resmi lig maçında ise İzmir Ülküspor ile 2-2 berabere kaldı.
        1974-75 sezonunda şampiyon olan Orduspor, gücünü ve başarısını Türkiye
        Birinci Ligi'nde göstermeye başladı. Ordululara Birinci Lig heyecanını
        yaşatan Orduspor, 4 sezon sonra Türkiye'nin en iyi takımları arasında
        dördüncülüğe yükseldi ve UEFA kupasına katılarak Orduspor'un adını
        Avrupa'ya taşımayı başardı.</p>

